package com.example.carwash.objeto;

public class Lavagem {

    private Integer idLavagem;
    private String veiculo;
    private String placa;
    private String servico;
    private Integer valorServico;
    private String cliente;
    private String usuario;
    private String hora;
    private String data;

    public Integer getIdLavagem() {
        return idLavagem;
    }

    public void setIdLavagem(Integer idLavagem) {
        this.idLavagem = idLavagem;
    }

    public String getVeiculo() {
        return veiculo;
    }

    public void setVeiculo(String veiculo) {
        this.veiculo = veiculo;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getServico() {
        return servico;
    }

    public void setServico(String servico) {
        this.servico = servico;
    }

    public Integer getValorServico() {
        return valorServico;
    }

    public void setValorServico(Integer valorServico) {
        this.valorServico = valorServico;
    }

    public String getCliente() {
        return cliente;
    }

    public void setCliente(String cliente) {
        this.cliente = cliente;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
